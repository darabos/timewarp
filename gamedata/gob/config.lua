function InitPlanets( planets )
	for a, b in planets do
		AddPlanet (b.name, b.x, b.y );
	end
end
	
function InitSpaceStations ( stations )
	for a, b in stations do
		AddStation (b.name, b.x, b.y, b.buildtype, b.commander, b. orbit, b.sprite, b.background )
	end
end
function InitStarSystem ()
	for a, b in Stars do
		if b.name == "GOB Star" then
			InitPlanets( b.planets );
			InitSpaceStations(b.starbases);
		end
	end
end

InitStarSystem();

